package com.youreln.cloudpad.utils

object TextCleaner {
    // 去空行
    fun removeEmptyLines(text: String): String {
        return text.lines().filter { it.isNotBlank() }.joinToString("\n")
    }

    // 去空格（首尾空格和多余空格）
    fun removeSpaces(text: String): String {
        return text.trim().replace("\\s+".toRegex(), " ")
    }

    // 全角转半角
    fun fullwidthToHalfwidth(text: String): String {
        val result = StringBuilder()
        for (char in text) {
            when (char.code) {
                in 65281..65374 -> result.append((char.code - 65248).toChar())
                12288 -> result.append(' ')
                else -> result.append(char)
            }
        }
        return result.toString()
    }

    // 半角转全角
    fun halfwidthToFullwidth(text: String): String {
        val result = StringBuilder()
        for (char in text) {
            when (char.code) {
                in 33..126 -> result.append((char.code + 65248).toChar())
                32 -> result.append(12288.toChar())
                else -> result.append(char)
            }
        }
        return result.toString()
    }

    // 中文标点转英文标点
    fun puncToEnglish(text: String): String {
        val map = mapOf(
            '，' to ',', '。' to '.', '！' to '!', '？' to '?',
            '；' to ';', '：' to ':', '"' to '"', '"' to '"',
            '‘' to "'", '’' to "'", '（' to '(', '）' to ')',
            '【' to '[', '】' to ']', '《' to '<', '》' to '>'
        )
        return text.map { map.getOrDefault(it, it) }.joinToString("")
    }

    // 英文标点转中文标点
    fun puncToChinese(text: String): String {
        val map = mapOf(
            ',' to '，', '.' to '。', '!' to '！', '?' to '？',
            ';' to '；', ':' to '：', '"' to '"', '"' to '"',
            "'" to '‘', "'" to '’', '(' to '（', ')' to '）',
            '[' to '【', ']' to '】', '<' to '《', '>' to '》'
        )
        return text.map { map.getOrDefault(it, it) }.joinToString("")
    }

    // 转大写
    fun toUpperCase(text: String): String {
        return text.uppercase()
    }

    // 转小写
    fun toLowerCase(text: String): String {
        return text.lowercase()
    }

    // 首字母大写
    fun toTitleCase(text: String): String {
        return text.split(" ").joinToString(" ") {
            if (it.isBlank()) it else it.replaceFirstChar { char -> char.uppercase() }
        }
    }

